
function makeAlbum(artist, title) {
  var albumObject = {
    "artist": artist,
    "title": title
  };
  return albumObject;
}

console.log(makeAlbum("Atif Aslam", "Meri Kahani"));
console.log(makeAlbum("Nusrat Fateh Ali Khan", "Pyar Karte Hain"));
console.log(makeAlbum("Arijit Singh", "Aashiqui 2"));
